package at.sarah.oo.Calculator;

public class BaseCalculator {

    public double add (int a, int b){
        return a + b;
    }

    public double subtract (int a, int b){
        return a - b;
    }

    public double divide (int a, int b){
        return (double) a / b;
    }

    public double multiply (int a, int b){
        return a * b;
    }
}
